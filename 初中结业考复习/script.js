// 7. 单题状态回显 (核心解耦逻辑)
// ==========================================
function restoreSingleQuestionState(quiz, record) {
    const quizDiv = document.querySelector(`.quiz-item[data-id="${quiz.id}"]`);
    if (!quizDiv) return;

    const redoBtn = quizDiv.querySelector('.redo-q-btn');
    if (redoBtn) redoBtn.style.display = "inline-block";

    if (quiz.type === "choice") {
        const allLabels = quizDiv.querySelectorAll(`.option-label`);
        allLabels.forEach(l => { l.classList.add("disabled"); l.style.pointerEvents = "none"; });
        
        if (record.userAnswer !== null && record.userAnswer !== undefined) {
            const userLabel = quizDiv.querySelector(`label[data-original-idx="${record.userAnswer}"]`);
            const correctLabel = quizDiv.querySelector(`label[data-original-idx="${quiz.answer}"]`);
            
            if (record.isCorrect) {
                if(userLabel) { userLabel.classList.add("correct"); userLabel.querySelector('input').checked = true; }
            } else {
                if(userLabel) { userLabel.classList.add("wrong"); userLabel.querySelector('input').checked = true; }
                if(correctLabel) correctLabel.classList.add("correct");
            }
        } else {
            const correctLabel = quizDiv.querySelector(`label[data-original-idx="${quiz.answer}"]`);
            if(correctLabel) correctLabel.classList.add("correct");
        }
    } 
    else if (quiz.type === "fill") {
        const inputs = quizDiv.querySelectorAll('.code-blank');
        inputs.forEach((input, i) => {
            input.readOnly = true;
            const expected = input.dataset.answer.trim().toLowerCase();
            const actual = (record.userAnswer && record.userAnswer[i]) ? record.userAnswer[i] : "";
            
            if (actual === expected) {
                input.value = actual;
                input.classList.add("correct");
            } else {
                input.value = actual ? `${actual} (答:${expected})` : `(答:${expected})`;
                input.classList.add("wrong");
            }
        });
    }

    const expEl = document.getElementById(`exp_${quiz.id}`);
    if (expEl) expEl.style.display = "block";
    const timeEl = document.getElementById(`time_${quiz.id}`);
    if (timeEl) timeEl.innerHTML = `<i class="fa-regular fa-clock"></i> 耗时: ${record.timeSpent}s`;
}

