// 8. 漏题拦截与自动阅卷
// ==========================================
function evaluateQuiz() {
    if(activeQid) { qTimers[activeQid] += (Date.now() - activeStartTime); activeQid = null; }

    let missingQuestions = [];
    
    // 检测漏题：跳过那些已经有记录的题目
    currentQuizzes.forEach((quiz, idx) => {
        if (userRecords[quiz.id]) return; 

        if (quiz.type === "choice") {
            if (!document.querySelector(`.quiz-item[data-id="${quiz.id}"] input[type="radio"]:checked`)) {
                missingQuestions.push(idx + 1);
            }
        } else if (quiz.type === "fill") {
            let allFilled = true;
            document.querySelectorAll(`.quiz-item[data-id="${quiz.id}"] .code-blank`).forEach(inp => {
                if (!inp.value.trim()) allFilled = false;
            });
            if (!allFilled) missingQuestions.push(idx + 1);
        }
    });

    if (missingQuestions.length > 0) {
        showCustomConfirm(
            "漏题警告", 
            `您还有 ${missingQuestions.length} 道题未作答！\n(第 ${missingQuestions.join(', ')} 题为空)\n\n确定要强行交卷吗？未作答将计为错误。`,
            doEvaluate
        );
    } else {
        doEvaluate();
    }
}

function doEvaluate() {
    let correctCount = 0;
    let totalTime = 0;

    currentQuizzes.forEach(quiz => {
        // 如果题目已经有记录，直接读取成绩，跳过DOM判卷
        if (userRecords[quiz.id]) {
            if (userRecords[quiz.id].isCorrect) correctCount++;
            totalTime += userRecords[quiz.id].timeSpent;
            return; 
        }

        // 否则，进行新题判卷
        let isCorrect = false;
        let userAnswer = null;
        const timeSpent = Math.round(qTimers[quiz.id] / 1000) || 1; 
        totalTime += timeSpent;

        if (quiz.type === "choice") {
            const selectedOpt = document.querySelector(`.quiz-item[data-id="${quiz.id}"] input[type="radio"]:checked`);
            if (selectedOpt) {
                userAnswer = parseInt(selectedOpt.value);
                if (userAnswer === quiz.answer) isCorrect = true;
            }
        } else if (quiz.type === "fill") {
            const inputs = document.querySelectorAll(`.quiz-item[data-id="${quiz.id}"] .code-blank`);
            let allInputsCorrect = true;
            userAnswer = [];
            inputs.forEach(input => {
                const expected = input.dataset.answer.trim().toLowerCase();
                const actual = input.value.trim().toLowerCase();
                userAnswer.push(actual);
                if (actual !== expected) allInputsCorrect = false;
            });
            isCorrect = allInputsCorrect;
        }

        if (isCorrect) correctCount++;
        userRecords[quiz.id] = { userAnswer, isCorrect, timeSpent };

        // 判卷完毕，立刻调用回显函数渲染颜色和解析
        restoreSingleQuestionState(quiz, userRecords[quiz.id]);
    });

    const score = Math.round((correctCount / currentQuizzes.length) * 100);
    unitStatus[currentTopicId] = { isCompleted: true, score: score, totalTime: totalTime, date: new Date().toLocaleString() };

    saveToLocal();
    showToast(`交卷成功！得分: ${score}分`, "success");
    renderSidebar(); 
    
    document.getElementById("submit-btn").style.display = "none";
    document.getElementById("clear-unit-btn").style.display = "inline-flex";
    const badge = document.getElementById("unit-status-badge");
    badge.innerText = "已完成"; badge.className = "status-badge completed";
    
    showResultBoard(score, totalTime);
}

function showResultBoard(score, totalTime) {
    const resultArea = document.getElementById("result-area");
    resultArea.style.display = "flex";
    document.getElementById("final-score").innerText = score;
    document.getElementById("time-stats-desc").innerHTML = `<i class="fa-regular fa-clock"></i> 本单元作答耗时: <b>${totalTime} 秒</b>`;
    
    const circle = resultArea.querySelector(".score-circle");
    if (score >= 90) { circle.style.background = "var(--secondary-color)"; document.getElementById("feedback-title").innerText = "优秀！所向披靡！"; } 
    else if (score >= 60) { circle.style.background = "var(--accent-color)"; document.getElementById("feedback-title").innerText = "及格！巩固一下错题吧。"; } 
    else { circle.style.background = "var(--danger-color)"; document.getElementById("feedback-title").innerText = "不及格，建议重做！"; }
}

