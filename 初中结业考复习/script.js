function exportJSON() {
    if(!currentUser) { showToast("请先登录！", "error"); return; }
    if(Object.keys(userRecords).length === 0) { showToast("暂无作答记录，无需导出。", "error"); return; }

    const exportData = {
        studentInfo: currentUser,
        exportTime: new Date().toLocaleString(),
        records: userRecords,
        status: unitStatus
    };
    const dataStr = "data:text/json;charset=utf-8," + encodeURIComponent(JSON.stringify(exportData, null, 2));
    const a = document.createElement('a');
    a.href = dataStr;
    a.download = `信息科技答题报告_${currentUser.name}_${new Date().getTime()}.json`;
    document.body.appendChild(a);
    a.click();
    a.remove();
    showToast("导出成功！", "success");
}

function importJSON(e) {
    const file = e.target.files[0];
    if (!file) return;
    const reader = new FileReader();
    reader.onload = function(evt) {
        try {
            const importedData = JSON.parse(evt.target.result);
            if (importedData.studentInfo && importedData.records) {
                currentUser = importedData.studentInfo;
                userRecords = importedData.records;
                unitStatus = importedData.status || {};
                localStorage.setItem(STORAGE_USER, JSON.stringify(currentUser));
                saveToLocal();
                alert("导入成功！");
                location.reload();
            } else { showToast("非法的存档文件格式！", "error"); }
        } catch (err) { showToast("文件读取失败！", "error"); }
    };
    reader.readAsText(file);
    e.target.value = ""; 
}

function showToast(message, type = "success") {
    const container = document.getElementById("toast-container");
    const toast = document.createElement("div");
    toast.className = `toast ${type}`;
    toast.innerHTML = type === "success" ? `<i class="fa-solid fa-check-circle"></i> ${message}` : `<i class="fa-solid fa-triangle-exclamation"></i> ${message}`;
    container.appendChild(toast);
    setTimeout(() => { toast.style.opacity = '0'; toast.style.transform = 'translateY(-20px)'; setTimeout(() => toast.remove(), 300); }, 3000);
}