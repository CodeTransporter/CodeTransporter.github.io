// 6. 加载单元与防作弊渲染
// ==========================================
function loadTopic(id) {
    currentTopicId = id;
    const topic = knowledgeData.find(k => k.id === id);
    if (!topic) return;

    document.getElementById("welcome-screen").style.display = "none";
    document.getElementById("knowledge-section").style.display = "block";
    document.getElementById("quiz-section").style.display = "block";
    document.getElementById("result-area").style.display = "none";
    
    document.getElementById("k-grade").innerHTML = `<i class="fa-solid fa-layer-group"></i> ${topic.grade}年级`;
    document.getElementById("k-page").innerHTML = `<i class="fa-solid fa-book-open"></i> ${topic.page}`;
    document.getElementById("k-title").innerText = topic.title;
    document.getElementById("k-content").innerHTML = topic.officialText;
    document.getElementById("k-humor").innerHTML = topic.humorText;

    // 洗牌算法打乱题目顺序
    let rawQuizzes = quizData.filter(q => q.knowledgeId === id);
    currentQuizzes = shuffleArray(rawQuizzes); 
    
    document.getElementById("q-count").innerText = currentQuizzes.length;
    
    qTimers = {};
    currentQuizzes.forEach(q => qTimers[q.id] = 0);
    activeQid = null;
    activeStartTime = Date.now();

    renderQuizzes(currentQuizzes);

    // 【核心修复】无论单元是否完成，都要逐题恢复已作答的题目状态
    let answeredCount = 0;
    currentQuizzes.forEach(quiz => {
        if (userRecords[quiz.id]) {
            restoreSingleQuestionState(quiz, userRecords[quiz.id]);
            answeredCount++;
        }
    });

    const status = unitStatus[id];
    const badge = document.getElementById("unit-status-badge");
    
    // 如果单元已完成且所有题都答了，显示总分板
    if (status && answeredCount === currentQuizzes.length) {
        badge.innerText = "已完成";
        badge.className = "status-badge completed";
        document.getElementById("submit-btn").style.display = "none";
        document.getElementById("clear-unit-btn").style.display = "inline-flex";
        showResultBoard(status.score, status.totalTime);
    } else {
        badge.innerText = answeredCount > 0 ? "挑战中" : "未作答";
        badge.className = "status-badge";
        document.getElementById("submit-btn").style.display = "inline-flex";
        document.getElementById("clear-unit-btn").style.display = "none";
    }
}

function renderQuizzes(quizzes) {
    const listEl = document.getElementById("quiz-list");
    listEl.innerHTML = "";
    
    if (quizzes.length === 0) {
        listEl.innerHTML = "<p style='color:#777; text-align:center;'>该考点暂无试题。</p>";
        document.getElementById("quiz-actions").style.display = "none";
        return;
    }
    document.getElementById("quiz-actions").style.display = "flex";

    quizzes.forEach((quiz, index) => {
        const div = document.createElement("div");
        div.className = "quiz-item";
        div.dataset.id = quiz.id;

        let html = `<div class="q-title">
                        <span class="q-index">${index + 1}</span>. ${quiz.question} 
                        <button class="redo-q-btn" data-qid="${quiz.id}" style="display:none;" onclick="clearSingleQuestion('${quiz.id}')"><i class="fa-solid fa-rotate-right"></i> 重做此题</button>
                    </div>`;

        if (quiz.type === "choice") {
            html += `<ul class="options-list">`;
            // 洗牌选项
            let mappedOptions = quiz.options.map((opt, idx) => ({ text: opt, originalIdx: idx }));
            let shuffledOptions = shuffleArray(mappedOptions);

            shuffledOptions.forEach((optObj, optIdx) => {
                const labelLetter = ["A", "B", "C", "D"][optIdx];
                html += `
                    <label class="option-label" data-quiz="${quiz.id}" data-original-idx="${optObj.originalIdx}">
                        <input type="radio" name="q_${quiz.id}" value="${optObj.originalIdx}">
                        <span>${labelLetter}. ${optObj.text}</span>
                    </label>`;
            });
            html += `</ul>`;
        }

        html += `<div class="explanation" id="exp_${quiz.id}" style="display:none;">
                    <i class="fa-solid fa-lightbulb"></i> <b>解析：</b>${quiz.explanation}
                    <div class="time-spent" id="time_${quiz.id}"></div>
                 </div>`;

        div.innerHTML = html;
        listEl.appendChild(div);
    });

    bindQuizEvents(listEl);
}

function bindQuizEvents(container) {
    container.querySelectorAll('.option-label').forEach(label => {
        label.addEventListener('click', function() {
            if(this.classList.contains('disabled')) return; 
            updateTimer(this.dataset.quiz);
            const parent = this.parentElement;
            parent.querySelectorAll('.option-label').forEach(l => l.classList.remove('selected'));
            this.classList.add('selected');
            this.querySelector('input').checked = true;
        });
    });

    container.querySelectorAll('.code-blank').forEach(input => {
        input.addEventListener('focus', function() { updateTimer(this.closest('.quiz-item').dataset.id); });
        input.addEventListener('input', function() { updateTimer(this.closest('.quiz-item').dataset.id); });
    });
}

function updateTimer(newQid) {
    const now = Date.now();
    if (activeQid) qTimers[activeQid] += (now - activeStartTime);
    activeQid = newQid;
    activeStartTime = now;
}

