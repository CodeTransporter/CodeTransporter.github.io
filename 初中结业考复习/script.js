function renderSidebar(keyword = "") {
    const listEl = document.getElementById("topic-list");
    listEl.innerHTML = "";
    
    let filteredData = knowledgeData.filter(k => k.grade === currentGrade);
    if (keyword) {
        filteredData = filteredData.filter(k => k.title.toLowerCase().includes(keyword) || k.officialText.toLowerCase().includes(keyword));
    }

    if (filteredData.length === 0) {
        listEl.innerHTML = `<li class="topic-item"><span style="color:#999;">未找到考点</span></li>`;
        return;
    }

    filteredData.forEach(item => {
        const li = document.createElement("li");
        li.className = "topic-item";
        if (item.id === currentTopicId) li.classList.add("active");
        
        let statusHtml = "";
        if (unitStatus[item.id] && unitStatus[item.id].isCompleted) {
            statusHtml = `<span class="topic-status"><i class="fa-solid fa-check"></i> ${unitStatus[item.id].score}分</span>`;
        }

        li.innerHTML = `<h4>${item.title} ${statusHtml}</h4><div class="topic-meta">${item.page}</div>`;
        li.addEventListener("click", () => {
            document.querySelectorAll(".topic-item").forEach(el => el.classList.remove("active"));
            li.classList.add("active");
            if(window.innerWidth <= 768) document.getElementById("close-sidebar-btn").click();
            loadTopic(item.id);
        });
        listEl.appendChild(li);
    });
}

